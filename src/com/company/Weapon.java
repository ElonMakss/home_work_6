package com.company;

public class Weapon {
    private String type;
    private String name;

    public String getType() {
        return type;
    }

    public Weapon setType(String type) {
        this.type = type;
        return this;
    }

    public String getName() {
        return name;
    }

    public Weapon setName(String name) {
        this.name = name;
        return this;
    }
}

